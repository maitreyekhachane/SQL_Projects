--ROUTE PROFITABILITY
WITH ROUTEPROFIT AS
(SELECT SOURCE,DESTINATION,SUM(TOTAL_FARE) AS TOTAL_REVENUE,SUM(DISTANCE) AS TOTAL_DISTANCE FROM RIDES
GROUP BY SOURCE,destination)

SELECT *,
TOTAL_REVENUE / TOTAL_DISTANCE AS PROFITPERKM
FROM ROUTEPROFIT;
--CERTAIN ROUTES GENERATE HIGHER REVENUE PER KM INDICATING BETTER PROFITABILITY.


--RANK ROUTES BY REVENUE USING WINDOW FUNCTIONS
SELECT SOURCE,DESTINATION,SUM(TOTAL_FARE) AS REVENUE,
RANK() OVER(ORDER BY SUM(TOTAL_FARE) DESC) AS RANKROUTES
FROM RIDES
GROUP BY SOURCE,DESTINATION;
--ROUTES CAN BE RANKED BASED ON TOTAL REVENUE TO IDENTIFY TOP PERFORMING ROUTES.


